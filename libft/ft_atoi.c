#include "libft.h"

int	ft_atoi(const char *str)
{
	long long int	res;
	int				sign;
	int				i;

	i = 0;
	sign = 1;
	res = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		res = res * 10 + str[i++] - 48;
	}
	return (res * sign);
}
