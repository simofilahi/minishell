#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	len;

	i = 0;
	len = ft_strlen(s1);
	while (n-- && (s1[len + i] = s2[i]))
		i++;
	s1[len + i] = '\0';
	return (s1);
}
